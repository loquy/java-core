package _1_Variable;

public class Variable {
    public static void main(String[] args) {
        int i = 123;
        float f = 3.14f;
        double d = 3.14;
        boolean b = true;
        char symbol = '@';
        String name = "Bro";

        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(symbol);
        System.out.println("hello " + name);
    }
}
