package main;

public class WrapperClasses_17 {
    public static void main(String[] args) {
        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Bro";

        if(a==true) {
            System.out.println("Boolean, This is true");
        }
        if(b=='@') {
            System.out.println("Character, This is true");
        }
        if(c == 123) {
            System.out.println("Integer, This is true");
        }
        if(d == 3.14) {
            System.out.println("Double, This is true");
        }
        if(e == "Bro") {
            System.out.println("String, This is true");
        }
    }
}
